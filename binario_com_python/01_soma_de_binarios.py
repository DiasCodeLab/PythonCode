#=============================================
# Soma entre binarios com resultado em binario 
#=============================================


soma1 = 0b0000000001
soma2 = 0b0000000010

calculo = soma1 + soma2

# em numero inteiro 
print(f'O resultado de soma 1 mais soma 2 é: {calculo}')

# em binario 
print(f'Oresultado em binario é {calculo:010b}')